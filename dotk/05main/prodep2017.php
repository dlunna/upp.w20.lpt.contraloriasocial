  <p>Conforme a lo dispuesto por la Ley General de Desarrollo Social, la Contraloría Social constituye una práctica de transparencia, de rendición de cuentas y se convierte en un mecanismo para los beneficiarios, para que de manera
    organizada verifiquen el cumplimiento de las metas y la correcta aplicación de los recursos públicos asignados a diferentes Programas Federales.</p>
